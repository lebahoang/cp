import sys
def find_max_in_array(arr, k):
    n = len(arr)
    dp = [[0 for j in range(k)] for i in range(n)]
    for i in range(n):
        if i == 0:
            dp[i][0] = arr[i]
        else:
            dp[i][0] = max(dp[i-1][0], arr[i])
    for j in range(1, k):
        for i in range(1, n):
            if j > i: continue
            dp[i][j] = max(dp[i-1][j], dp[i-1][j-1]*10+arr[i])
    return dp


def cmp(a,b,i,j):
    ind1 = i
    ind2 = j
    while ind1 < len(a) and ind2 < len(b):
        if a[ind1] > b[ind2]:
            return 1
        elif a[ind1] < b[ind2]:
            return 2
        ind1 += 1
        ind2 += 1
    if ind1 < len(a):
        return 1
    if ind2 < len(b):
        return 2
    return 0
        

def merge(a,b):
    a = [int(s) for s in str(a)]
    b = [int(s) for s in str(b)]
    n = len(a)
    m = len(b)
    i = 0
    j = 0
    rs = []
    while i < n and j < m:
        if a[i] > b[j]:
            rs.append(a[i])
            i += 1
        elif a[i] < b[j]:
            rs.append(b[j])
            j += 1
        else:
            x = cmp(a,b,i,j)
            if x == 1:
                rs.append(a[i])
                i += 1
            elif x == 2:
                rs.append(b[j])
                j += 1
            else:
                rs.append(a[i])
                i += 1

    if i < len(a):
        rs.extend(a[i:])
    if j < len(b):
        rs.extend(b[j:])

    c = 0
    for i in rs:
        c = c*10+i
    return c
            
def solve321(a, b, k):
    n = len(a)
    m = len(b)
    k1 = min(n, k)
    k2 = min(m, k)
    dp1 = find_max_in_array(a, k1)
    dp2 = find_max_in_array(b, k2)

    maxV = 0
    for i in range(k1):
        if k-i-2 < 0 or k-i-2 >= k2: continue
        x = dp1[n-1][i]
        y = dp2[m-1][k-i-2]
        # print(i, x, k-i-2, y)
        z = merge(x,y)
        if maxV < z:
            maxV = z
    if k1 >= k:
        maxV = max(maxV, dp1[n-1][k-1])
    if k2 >= k:
        maxV = max(maxV, dp2[m-1][k-1])
    return [int(s) for s in str(maxV)]

